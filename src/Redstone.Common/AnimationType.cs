namespace Redstone.Common
{
    /// <summary>
    /// Represents the different entity animation types.
    /// </summary>
    public enum AnimationType
    {
        /// <summary>
        /// Swing main arm animation.
        /// </summary>
        SwingMainArm,

        /// <summary>
        /// Damage animation.
        /// </summary>
        TakeDamage,

        /// <summary>
        /// Leave bed animation.
        /// </summary>
        LeaveBed,

        /// <summary>
        /// Swing off hand animation.
        /// </summary>
        SwingOffHand,

        /// <summary>
        /// Critical effect animation.
        /// </summary>
        CriticalEffect,

        /// <summary>
        /// Magic critical effect.
        /// </summary>
        MagicCriticalEffect
    }
}
